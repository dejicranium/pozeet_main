class ListUtil:

	@classmethod
	def dict_keys_to_list(dictt):
		assert isinstance(dict, dictt)
		return [key for key in dictt.keys()]

	@classmethod
	def dict_values_to_list(dictt):
		assert isinstance(dict, dictt)
		return [val for val in dictt.values()]


